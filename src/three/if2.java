package three;

public class if2 {
    public static void main(String[] args) {

        int number = 1;

        if (number > 0) {
            System.out.println("number 는 양수입니다.");
        } else if (number < 0) {
            System.out.println("number 는 음수입니다");
        } else {
            System.out.println("number 0입니다");
        }
    }
}
