package three;

public class Switch_IF {
    public static void main(String[] args) {

        String mood = "happy";

        if (mood == "happy") {
            System.out.println("행복");
        } else if (mood == "angry") {
            System.out.println("화남");
        } else if (mood == "sad") {
            System.out.println("불행");
        } else {
            System.out.println("무감정");
        }
    }



}
